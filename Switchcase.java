package Javac;

public class Switchcase {

	public static void main(String[] args) {
		
		int day=4;
		
		switch(day) {
		
		case 1:
				System.out.println("sunday");
		break;
		case 2:
				System.out.println("monday");
		break;
		case 3:
				System.out.println("tuesday");
		break;
		case 4:
				System.out.println("wednsday");
		break;
		default :
			System.out.println("wrong input");	
		}
	}
}
